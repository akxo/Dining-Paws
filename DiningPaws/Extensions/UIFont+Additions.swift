import UIKit

extension UIFont {
    
    static var chalkboardLight15: UIFont? {
        return UIFont(name: "ChalkboardSE-Light", size: 15)
    }
    
    static var chalkboardRegular18: UIFont? {
        return UIFont(name: "ChalkboardSE-Regular", size: 18)
    }
    
    static var chalkboardBold20: UIFont? {
        return UIFont(name: "ChalkboardSE-Bold", size: 20)
    }
}
